const categories = Object.keys(pools);
const messages = [];
const seen = new Set();

for (const category of categories) {
  for (const text of pools[category]) {
    if (seen.has(text)) continue;
    seen.add(text);
    messages.push({ id: messages.length + 1, category, text });
  }
}

// Complementa até 200 com variações únicas
const suffixes = [
  " Que este momento inspire gentileza.",
  " Leve esta mensagem com você hoje.",
  " Pequenos gestos constroem grandes mudanças.",
  " A bondade começa com uma escolha.",
  " O mundo melhora quando nos importamos.",
  " Há beleza em cuidar uns dos outros.",
  " A esperança vive nos gestos do dia a dia.",
  " Compartilhe luz onde puder.",
  " A compaixão é sempre uma boa resposta.",
  " Juntos, escrevemos histórias de recomeço.",
];

let i = 0;
while (messages.length < 200) {
  const base = messages[i % messages.length];
  const variant = `${base.text.replace(/\.$/, "")} — ${suffixes[i % suffixes.length]}`;
  if (!seen.has(variant)) {
    seen.add(variant);
    messages.push({
      id: messages.length + 1,
      category: base.category,
      text: variant,
    });
  }
  i++;
  if (i > 5000) break;
}

const payload = {
  version: 1,
  total: 200,
  messages: messages.slice(0, 200),
};

await writeFile(output, `${JSON.stringify(payload, null, 2)}\n`, "utf8");
console.log(`Geradas ${payload.messages.length} mensagens em ${output}`);
